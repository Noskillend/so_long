#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	pm;
	int	nb;

	i = 0;
	pm = 1;
	nb = 0;
	while (str[i] == ' ' || (str[i] > 8 && str[i] < 14))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			pm = -pm;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	return (nb * pm);
}
